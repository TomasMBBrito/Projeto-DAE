<template>
  <div class="create-publication">
    <nav class="navbar">
      <div class="navbar-brand">
        <h1>Create Publication</h1>
      </div>
      <div class="navbar-actions">
        <button @click="goBack" class="btn-nav">Back</button>
        <button @click="goToProfile" class="btn-nav">Profile</button>
        <button @click="goLogout" class="btn-logout">Logout</button>
      </div>
    </nav>

    <div class="form-container">
      <form @submit.prevent="handleSubmit" class="pub-form" enctype="multipart/form-data">

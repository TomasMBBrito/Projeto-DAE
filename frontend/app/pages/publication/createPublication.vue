          <label>Title *</label>
          <input v-model="form.title" type="text" required />
        </div>

        <div class="form-row">
          <label>Summary</label>
          <textarea v-model="form.summary" rows="3" placeholder="Optional — if empty, summary may be generated by backend"></textarea>
        </div>

        <div class="form-row">
          <label>Scientific Area *</label>
          <select v-model="form.scientificArea" required>
            <option disabled value="">Select area</option>
            <option value="COMPUTER_SCIENCE">COMPUTER_SCIENCE</option>
            <option value="BIOLOGY">BIOLOGY</option>
            <option value="CHEMISTRY">CHEMISTRY</option>
            <option value="PHYSICS">PHYSICS</option>
            <option value="MATHEMATICS">MATHEMATICS</option>
            <option value="ENGINEERING">ENGINEERING</option>
            <option value="ENVIRONMENTAL_SCIENCE">ENVIRONMENTAL_SCIENCE</option>
            <option value="SOCIAL_SCIENCES">SOCIAL_SCIENCES</option>
            <option value="HUMANITIES">HUMANITIES</option>
          </select>
        </div>

        <div class="form-row">
          <label>Publication Date *</label>
          <input v-model="form.publicationDate" type="date" required />
        </div>

        <div class="form-row">
          <label>Authors (comma separated)</label>
          <input v-model="form.authors" placeholder="Alice,Bob,Charlie" />
        </div>

        <div class="form-row">
          <label>Tags</label>
          <div class="tags-list">
            <button
              v-for="tag in availableTags"
              :key="tag.id"
              type="button"
              @click="toggleTag(tag.id)"
              :class="['tag-chip', { active: selectedTags.includes(tag.id) }]"
            >
              {{ tag.name }}
              <span v-if="selectedTags.includes(tag.id)" class="check-icon">✓</span>
            </button>
          </div>
        </div>

        <div class="form-row">
          <label>File (PDF)</label>
          <input ref="fileInput" type="file" accept=".pdf" />
        </div>

        <div class="form-actions">
          <button type="submit" :disabled="submitting" class="btn-submit">
            {{ submitting ? 'Submitting...' : 'Create Publication' }}
          </button>
          <button type="button" @click="resetForm" class="btn-reset">Reset</button>
        </div>

        <div v-if="error" class="error">{{ error }}</div>
        <div v-if="success" class="success">{{ success }}</div>
      </form>
    </div>
  </div>
</template>

<script setup>
import { ref, onMounted } from 'vue'
import { usePublicationStore } from '~/stores/publication-store'
import { useTagStore } from '~/stores/tag-store'
import { useAuthStore } from '~/stores/auth-store'

const publicationStore = usePublicationStore()
const tagStore = useTagStore()
const authStore = useAuthStore()
const router = useRouter()

const form = ref({
  title: '',
  summary: '',
  scientificArea: '',
  publicationDate: '',
  authors: ''
})

const availableTags = ref([])
const selectedTags = ref([])
const submitting = ref(false)
const error = ref('')
const success = ref('')

const fileInput = ref(null)

onMounted(async () => {
  await loadTags()
})

async function loadTags() {
  try {
    availableTags.value = await tagStore.getAll()
  } catch (e) {
    console.error('Failed to load tags', e)
  }
}

function toggleTag(tagId) {
  const idx = selectedTags.value.indexOf(tagId)
  if (idx > -1) selectedTags.value.splice(idx, 1)
  else selectedTags.value.push(tagId)
}

function resetForm() {
  form.value = {
    title: '',
    summary: '',
    scientificArea: '',
    publicationDate: '',
    authors: ''
  }
  selectedTags.value = []
  error.value = ''
  success.value = ''
  if (fileInput.value) fileInput.value.value = null
}

function goBack() {
  router.back()
}

function goToProfile() {
  const username = authStore.user?.username || authStore.username
  if (username) router.push(`/users/${username}`)
}

function goLogout() {
  router.push('/auth/login')
}

async function handleSubmit() {
  error.value = ''
  success.value = ''
  if (!form.value.title || !form.value.scientificArea || !form.value.publicationDate) {
    error.value = 'Title, Scientific Area and Publication Date are required.'
    return
  }

  submitting.value = true

  try {
    const fd = new FormData()
    fd.append('title', form.value.title)
    fd.append('summary', form.value.summary || '')
    fd.append('scientificArea', form.value.scientificArea)
    // publicationDate must be yyyy-mm-dd
    fd.append('publicationDate', form.value.publicationDate)
    // authors as comma separated string
    if (form.value.authors && form.value.authors.trim() !== '') {
      fd.append('authors', form.value.authors)
    }
    
    if (selectedTags.value.length > 0) {
      fd.append('tagIds', selectedTags.value.join(','))
    }
    // file input
    const fileEl = fileInput.value
    if (fileEl && fileEl.files && fileEl.files[0]) {
      fd.append('file', fileEl.files[0])
    }

    
    const resp = await publicationStore.create(fd)

    
    const id = resp?.id || (resp?.data && resp.data.id)

    if (id) {
      success.value = 'Publication created successfully.'
    
      router.push(`/publication/searchPublications`)
    } else {
      // se backend devolveu processamento assíncrono (202) pode devolver message + id
      if (resp?.status === 202 && resp?.data?.id) {
        router.push(`/publication/searchPublications`)
      } else {
        success.value = 'Publication created. Check server response.'
        // se quiseres, podes mostrar resp (console) para debugging
        console.log('create response', resp)
      }
    }
  } catch (e) {
    console.error(e)
    error.value = e?.message || 'Failed to create publication'
  } finally {
    submitting.value = false
  }
}
</script>

